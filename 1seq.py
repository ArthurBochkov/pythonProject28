"""
4. (МОДУЛЬ 1) Создать новый проект, в нем создать модуль 1seq.py. Задание:
Пользователь вводит количество элементов будущего списка
После этого по очереди по одной вводит любые цифры
Сохранить цифры в список
Отсортировать список по возрастанию и вывести на экран
Пример работы: Введите количество элементов: 3
Введите 1 элемент: 5
Введите 2 элемент: 2
Введите 3 элемент: 4
Вывод: [2, 4, 5]
"""

# Запрашиваем у пользователя количество элементов
num_elements = int(input("Введите количество элементов: "))

# Создаем пустой список для хранения элементов
numbers = []

# Заполняем список элементами, введенными пользователем
for i in range(num_elements):
    while True:
        try:
            # Запрашиваем элемент и преобразуем его в число
            element = int(input(f"Введите {i + 1} элемент: "))
            numbers.append(element)
            break
        except ValueError:
            # Если ввод некорректный, просим пользователя повторить
            print("Ошибка! Введите целое число.")

# Сортируем список по возрастанию
numbers.sort()

# Выводим отсортированный список
print("Вывод:", numbers)