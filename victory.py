"""
7. (МОДУЛЬ 4) В проекте создать новый модуль victory.py. Задание
Написать или улучшить программу Викторина из предыдущего дз (Для тренировки предлагаю не пользоваться никакими библиотеками кроме random)
Есть 10 известных людей и их даты рождения в формате '02.01.1988' ('dd.mm.yyyy') - предлагаю для тренировки пока использовать строку
Программа выбирает из этих 10-и 5 случайных людей, это можно реализовать с помощью модуля random и функции sample
Пример использования sample:
import random
numbers = [1, 2, 3, 4, 5]

# 2 - количество случайных элементов
result = random.sample(numbers, 2)

print(result) # [5, 1]

После того как выбраны 5 случайных людей, предлагаем пользователю ввести их дату рождения
пользователь вводит дату в формате 'dd.mm.yyyy'

Например 03.01.2009, если пользователь ответил неверно, то выводим правильный ответ, но уже в следующем виде: третье января 2009 года, склонением можно пренебречь

В конце считаем количество правильных и неправильных ответов и предлагаем начать снова
"""

import random

# Список известных людей и их дат рождения (в формате 'dd.mm.yyyy')
famous_people = {
    "Александр Пушкин": "06.06.1799",
    "Лев Толстой": "09.09.1828",
    "Михаил Ломоносов": "19.11.1711",
    "Дмитрий Менделеев": "08.02.1834",
    "Иван Павлов": "27.09.1849",
    "Сергей Королёв": "12.01.1907",
    "Юрий Гагарин": "09.03.1934",
    "Николай Коперник": "19.02.1473",
    "Альберт Эйнштейн": "14.03.1879",
    "Исаак Ньютон": "25.12.1642"
}

# Словарь для преобразования чисел в слова
months = {
    "01": "января", "02": "февраля", "03": "марта", "04": "апреля",
    "05": "мая", "06": "июня", "07": "июля", "08": "августа",
    "09": "сентября", "10": "октября", "11": "ноября", "12": "декабря"
}


# Функция для преобразования даты в текстовый формат
def date_to_text(date):
    day, month, year = date.split('.')
    return f"{day} {months[month]} {year} года"


# Основной цикл программы
while True:
    # Выбираем 5 случайных людей из списка
    selected_people = random.sample(list(famous_people.items()), 5)

    correct_answers = 0
    incorrect_answers = 0

    print("\nДобро пожаловать в викторину!")
    print("Вам нужно ввести дату рождения для каждого из 5 известных людей.")

    for person, birth_date in selected_people:
        user_input = input(f"Введите дату рождения {person} (в формате dd.mm.yyyy): ")
        if user_input == birth_date:
            print("Правильно!")
            correct_answers += 1
        else:
            print(f"Неправильно. Правильный ответ: {date_to_text(birth_date)}")
            incorrect_answers += 1

    # Выводим статистику
    print("\nРезультаты:")
    print(f"Правильных ответов: {correct_answers}")
    print(f"Неправильных ответов: {incorrect_answers}")

    # Предлагаем начать заново
    play_again = input("Хотите сыграть еще раз? (да/нет): ").lower()
    if play_again not in ["да", "yes"]:
        print("Спасибо за игру! До свидания!")
        break